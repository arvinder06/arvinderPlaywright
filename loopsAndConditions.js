const flag = true

// IF ELSE

if (flag) {
    console.log("The flag is TRUE")
} else {
    console.log("The flag is FALSE")
}

if (!flag) {
    console.log("The flag is TRUE")
} else {
    console.log("The flag is FALSE")
}

console.log(flag)

// While conditional loop
let i = 0
while (i <= 10) {
    console.log("i=" + i)
    i++
}

//DO WHILE loop
let j = 0
do {
    console.log("j=" + j)
    j++
} while (j < 10);

// FOR Loop

for (let k = 0; k < 10; k++) {
    console.log("K=" + k)
}